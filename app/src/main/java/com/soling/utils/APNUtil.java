package com.soling.utils;

public class APNUtil {
    private String id, type, apn;

    public void setId(String id) {
        this.id = id;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setApn(String apn) {
        this.apn = apn;
    }

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getApn() {
        return apn;
    }

}
